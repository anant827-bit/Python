import turtle
scr=turtle.Screen()
scr.title("Heyyy")
scr.bgcolor("Pink")
t=turtle.Turtle()

t.forward(100)
t.goto(50,0)
t.right(90)
t.forward(100)
t.left(90)
t.goto(0,-100)
t.forward(100)

t.penup()
t.goto(150,0)
t.pendown()
t.right(90)
t.forward(100)
t.left(90)
t.forward(75)

t.penup()
t.goto(275,0)
t.pendown()
t.right(90)
t.forward(100)
t.left(90)
t.forward(75)
t.left(90)
t.forward(100)

turtle.done()